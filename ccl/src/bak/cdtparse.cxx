{if ((m_error=OnIdentifierTemplateLiteral())) return m_error;}
break;
case 430:
{if ((m_error=OnTemplateLiteral())) return m_error;}
break;
case 431:
{if ((m_error=OnIdentifierElementLiteral())) return m_error;}
break;
case 432:
{if ((m_error=OnIdentifierElementLiteral())) return m_error;}
